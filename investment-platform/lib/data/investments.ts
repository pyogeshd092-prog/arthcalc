// ─── HELPER FUNCTIONS (continued) ────────────────────────────────────────────

export function getInvestmentsByCategory(categoryId: string): InvestmentOption[] {
  return INVESTMENTS.filter(inv => inv.categoryId === categoryId);
}

export function getInvestmentsByIds(ids: string[]): InvestmentOption[] {
  return ids.map(id => INVESTMENTS.find(inv => inv.id === id)).filter(Boolean) as InvestmentOption[];
}

export function searchInvestments(query: string): InvestmentOption[] {
  const q = query.toLowerCase();
  return INVESTMENTS.filter(inv =>
    inv.name.toLowerCase().includes(q) ||
    inv.shortName.toLowerCase().includes(q) ||
    inv.tags.some(t => t.includes(q)) ||
    inv.description.toLowerCase().includes(q)
  );
}

export function getPopularInvestments(): InvestmentOption[] {
  return INVESTMENTS.filter(inv => inv.isPopular);
}

export function getAllInvestmentIds(): string[] {
  return INVESTMENTS.map(inv => inv.id);
}

